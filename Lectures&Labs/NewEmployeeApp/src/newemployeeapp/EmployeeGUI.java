    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        
        name = nameTxt.getText();
        try {
            empId = Integer.parseInt(idTxt.getText());

            for(int i = 0; i < empList.size();i++){
                if(empList.get(i).getName().equalsIgnoreCase(name) && empList.get(i).getId() == empId){

                    confDialog = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete "+empList.get(i).getName()+"?");

                    if(confDialog == JOptionPane.YES_OPTION) {
                        empList.remove(i);
                    }
                }
            }
            
            saveData(empList);
            clearFields();
            
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Please enter an Employee ID.");
        }
            
        saveData(empList);
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        
        name = nameTxt.getText();
        try {
            empId = Integer.parseInt(idTxt.getText());

            if(EmployeeRd.isSelected()) {
                type="Employee";
                Employee emp = new Employee(name,empId);
                empList.add(emp);
                JOptionPane.showMessageDialog(null, type+" added successfully.");
            } else if(EngineerRd.isSelected()) {
                type="Engineer";
                salary=Double.parseDouble(salaryTxt.getText());
                Engineer emp = new Engineer(name, empId,salary);
                empList.add(emp);
                JOptionPane.showMessageDialog(null, type+" added successfully.");
            } else if(ConsultantRd.isSelected()) {
                type="Consultant";
                hours=Integer.parseInt(hoursTxt.getText());
                rate=Double.parseDouble(rateTxt.getText());
                Consultant emp = new Consultant(name,empId,hours,rate);
                empList.add(emp);
                JOptionPane.showMessageDialog(null, type+" added successfully.");
            } else if(ManagerRd.isSelected()) {
                type="Manager";
                salary=Double.parseDouble(salaryTxt.getText());
                dept=deptTxt.getText();
                Manager emp = new Manager(name, empId, salary, dept);
                empList.add(emp);
                JOptionPane.showMessageDialog(null, type+" added successfully.");
            } else if(DirectorRd.isSelected()) {
                type="Director";
                salary=Double.parseDouble(salaryTxt.getText());
                dept=deptTxt.getText();
                budget=Double.parseDouble(budgetTxt.getText());
                Director emp = new Director(name, empId, salary, dept,budget);
                empList.add(emp);
                JOptionPane.showMessageDialog(null, type+" added successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Please select a type.");
            }
            
            saveData(empList);
            clearFields();
            
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Please enter an Employee ID.");
        }
        
        
        
    }//GEN-LAST:event_addBtnActionPerformed

