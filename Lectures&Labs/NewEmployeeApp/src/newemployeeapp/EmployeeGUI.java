    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        
        searchList = new ArrayList<Employee>();
        
        name = nameTxt.getText();
        try {
            empId = Integer.parseInt(idTxt.getText());
        } catch(NumberFormatException e) {
            empId = -1;
            System.out.println(e);
        }
            System.out.println(empId);
        for(int i = 0; i < empList.size(); i++) {
            if(empList.get(i).getName().equalsIgnoreCase(name) || empList.get(i).getId() == empId) {
                searchList.add(empList.get(i));
            }
        }
        confDialog=JOptionPane.showConfirmDialog(null,searchList.size()+" records found. \nTo show results press Yes.");
        if(confDialog == JOptionPane.YES_OPTION) {
            for(int j = 0; j < searchList.size(); j++) {
                JOptionPane.showMessageDialog(null,searchList.get(j).getDetails());     
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

